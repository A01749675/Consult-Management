        String[] selection = {"SÍ","NO"};
        int select = JOptionPane.showOptionDialog(null, "¿Segura que quieres regresar?", "Selección",JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE, null,selection,"Google" );

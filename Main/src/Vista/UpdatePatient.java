    private void VerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_VerMouseClicked
        // TODO add your handling code here:
        db.Connect();
        if(!buscar.getText().equals("") && db.Check_patient(buscar.getText())){
            db.View_patient_one(patients, buscar.getText());
            for(int i = 0; i<patients.getColumnCount();i++){
                switch(i){
                    case 0:
                        name.setText((String) patients.getValueAt(0, i));
                        break;
                    case 2:
                        Diagnosis.setText((String) patients.getValueAt(0, i));
                        break;
                    case 3:
                        Birthday.setText((String) patients.getValueAt(0,i));
                        break;
                    case 4:
                        Email.setText((String) patients.getValueAt(0, i));
                        break;
                    case 8:
                        cost.setText((String) patients.getValueAt(0, i));

                        
                }
            }
        }
        
    }//GEN-LAST:event_VerMouseClicked


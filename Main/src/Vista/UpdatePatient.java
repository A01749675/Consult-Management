    private void RegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RegistrarMouseClicked
        // TODO add your handling code here:
        if(!name.getText().equals("") && main.check_mail(Email.getText())){
            db.Connect();
            
            if(db.Update_patient(name.getText(), String.valueOf(gender.getItemAt(gender.getSelectedIndex())), Diagnosis.getText(), Birthday.getText(), Email.getText(), String.valueOf(Consult.getItemAt(Consult.getSelectedIndex())), String.valueOf(rfc.getItemAt(rfc.getSelectedIndex())), String.valueOf(status.getItemAt(status.getSelectedIndex())), cost.getText())){
                JOptionPane.showMessageDialog(this, "Paciente agregado", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(this, "ERROR", "Éxito", JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_RegistrarMouseClicked

